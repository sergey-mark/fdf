char		*ft_strcat(char *s1, char *s2)
{
	int i;
	int s;

	i = 0;
	s = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[s] != '\0')
	{
		s1[i] = s2[s];
		i++;
		s++;
	}
	s1[i] = '\0';
	return (s1);
}
